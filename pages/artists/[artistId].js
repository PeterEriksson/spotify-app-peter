import Head from "next/head";
import React, { useEffect, useState } from "react";
import Header from "../../components/Header";
import Sidebar from "../../components/Sidebar";
import useSpotify from "../../hooks/useSpotify";

export default function artistDetails({ artistId }) {
  const [artist, setArtist] = useState({});
  const [loading, setLoading] = useState(false);
  const [artistTopSongs, setArtistTopSongs] = useState([]);

  const spotifyApi = useSpotify();
  const getArtistInfo = () => {
    spotifyApi
      .getArtist(artistId)
      .then((data) => setArtist(data.body))
      .then(() => setLoading(false))
      .catch((err) => console.log(err));
  };

  useEffect(() => {
    setLoading(true);
    //ux friendly - display loading indicator a bit longer
    setTimeout(() => {
      getArtistInfo();
    }, 1200);
  }, []);

  /* Keep artist top songs?? */
  const getArtistTracks = () => {
    spotifyApi
      .getArtistTopTracks(artistId, "GB")
      .then((data) => setArtistTopSongs(data.body))
      .catch((err) => console.log(err));
  };
  useEffect(() => {
    getArtistTracks();
  }, []);

  return (
    <div
      onClick={() => console.log(artist)}
      className="flex h-screen bg-gray-800 "
    >
      <Head>
        <title>Artist info</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Sidebar />
      <div className=" w-screen bg-gray-800  overflow-y-scroll">
        {/* Pass down backArrow as prop? */}
        <Header backArrow />

        <div className="text-center text-white font-bold">
          <h1>{artist?.name}</h1>
          <h2>{artist?.popularity}</h2>

          <h3>{loading && "LOADING"}</h3>
        </div>
      </div>
    </div>
  );
}

export async function getServerSideProps({ params }) {
  const { artistId } = params;
  return {
    props: {
      artistId,
    },
  };
}
